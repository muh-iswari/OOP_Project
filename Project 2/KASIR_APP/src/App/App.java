    private void btn_hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hitungActionPerformed
        String Stringtotal = in_total.getText().replace("Rp. ", "");
        int total = Integer.parseInt(Stringtotal);
        int tunai = Integer.parseInt(in_tunai.getText());
        if (total > tunai){
            JOptionPane.showMessageDialog(null, "Saldo anda tidak mencukupi");
        } else {
            int kembalian = tunai - total;
            String StrKembalian = String.valueOf(kembalian);
            in_kembali.setText("Rp. " + StrKembalian);
        }
        
    }//GEN-LAST:event_btn_hitungActionPerformed

