    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        if (in_nama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama barang belum diinput");
        } else if (in_harga.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harga barang belum diinput");
        } else if (in_jumlah.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Jumlah barang belum diinput");
        } else {
           insertData();
           listProduct.add(new Product(in_nama.getText(), Integer.parseInt(in_harga.getText()),Integer.parseInt(in_jumlah.getText())));
           clear();
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int rowSelect = jTable1.getSelectedRow();
        int rowCount = model.getRowCount(); // Jumlah baris dalam model tabel

        if (btn_edit.getText().equals("Edit")) {
            if (rowCount > 0) { // Memastikan ada data dalam tabel
                if (rowSelect != -1) { // Memastikan ada baris yang dipilih
//                    updateTotal();
                    btn_tambah.setEnabled(false);
                    btn_delete.setEnabled(false);
                    btn_edit.setText("Update");
                    // Simpan nilai yang ada di sel terpilih ke dalam variabel
                    String oldValueNama = model.getValueAt(rowSelect, 1).toString();
                    String oldValueHarga = model.getValueAt(rowSelect, 2).toString();
                    String oldValueJumlah = model.getValueAt(rowSelect, 3).toString();

                    // Tampilkan nilai yang ada di sel terpilih di dalam teks field
                    in_nama.setText(oldValueNama);
                    in_harga.setText(oldValueHarga);
                    in_jumlah.setText(oldValueJumlah);
                } else {
                    JOptionPane.showMessageDialog(null, "Pilih baris terlebih dahulu.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Tambahkan data terlebih dahulu.");
            }
        } else if (btn_edit.getText().equals("Update")) {
            if (rowSelect != -1) { // Memastikan ada baris yang dipilih
                // Ambil nilai yang diubah dari teks field
//                updateTotal();
                if (in_nama.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Nama barang belum diinput");
                } else if (in_harga.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Harga barang belum diinput");
                } else if (in_jumlah.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Jumlah barang belum diinput");
                } else {
                    String newValueNama = in_nama.getText();
                    String newValueHarga = in_harga.getText();
                    String newValueJumlah = in_jumlah.getText();

                    // Update nilai pada tabel dengan nilai yang baru
                    model.setValueAt(newValueNama, rowSelect, 1);
                    model.setValueAt(newValueHarga, rowSelect, 2);
                    model.setValueAt(newValueJumlah, rowSelect, 3);

                    clear();
                    btn_tambah.setEnabled(true);
                    btn_delete.setEnabled(true);
                    btn_edit.setText("Edit");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pilih baris terlebih dahulu.");
            }
        }
    }//GEN-LAST:event_btn_editActionPerformed

